<template>
  <div class="epoch-circle">
    <q-circular-progress
      show-value
      class="q-mt-xs epoch-circle__progress"
      :value="epochProgress"
      size="106px"
      :thickness="0.2"
      color="natural-light-gray"
      track-color="warning"
      center-color="white"
    >
      <div class="epoch-circle__label">
        <div class="epoch-circle__label-title">Epoch</div>
        <div class="epoch-circle__label-number">
          {{ epochNumber }}
        </div>
        <div class="epoch-circle__label-value">{{ time.h }}:{{ time.m }}<br />{{ time.s }}</div>
      </div>
    </q-circular-progress>
  </div>
</template>

<script lang="ts">
  import { useEpochStore } from '@/store';
  import { computed, defineComponent } from 'vue';

  export default defineComponent({
    setup() {
      const epochStore = useEpochStore();
      return {
        epochNumber: computed(() => epochStore.epochNumber),
        epochProgress: computed(() => epochStore.epochProgress),
        time: computed(() => {
          const timeInMs = epochStore.epochTimeRemaining;
          const _h = timeInMs / 1000 / 60 / 60;
          const h = Math.floor(_h);
          const m = Math.floor((_h - h) * 60);
          const s = Math.ceil(((_h - h) * 60 - m) * 60);
          return { h, m: m < 10 ? `0${m}` : m, s: s < 10 ? `0${s}` : s };
        }),
      };
    },
  });
</script>

